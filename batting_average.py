class BattingAverage():
    def __init__(self, atBats, strikeOuts, hits):
        self.atBats = 0
        self.strikeOuts = 0
        self.hits = 0

    def atThePlate(atBats):
        self.atBats += 1
        print("Your average is: " + {self.hits}/{self.atBats})

    def strikeOut(atBats, strikeOuts):
        self.atBats += 1
        self.strikeOuts += 1
        print("Your average is: " + {self.hits}/{self.atBats})


    def getAHit(atBats, hits):
        self.atBats += 1
        self.hits += 1
        print("Your average is: " + {self.hits}/{self.atBats})